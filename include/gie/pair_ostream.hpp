//================================================================================================================================================
#include <ostream>
//================================================================================================================================================
namespace std
{
    template <class T1, class T2>
    std::ostream& operator<<(std::ostream& os, std::pair<T1, T2> const& pair) {
        os << "(" << pair.first << "," << pair.second << ")";
        return os;
    }
}
